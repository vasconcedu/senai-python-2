primeiro_numero = int(input("Primeiro numero: "))
segundo_numero = int(input("Segundo numero: "))

soma = primeiro_numero + segundo_numero
subtracao = primeiro_numero - segundo_numero
multiplicacao = primeiro_numero * segundo_numero
divisao = primeiro_numero / segundo_numero

print("A soma de {} e {} eh {}".format(primeiro_numero, segundo_numero, soma))
print("A subtracao de {} e {} eh {}".format(primeiro_numero, segundo_numero, subtracao))
print("A multiplicacao de {} e {} eh {}".format(primeiro_numero, segundo_numero, multiplicacao))
print("A divisao de {} e {} eh {}".format(primeiro_numero, segundo_numero, divisao))
